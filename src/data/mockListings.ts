function makePhotos(listingId: string, photoIndex: number, category: string) {
  const pool = CATEGORY_PHOTOS[category] ?? CATEGORY_PHOTOS['sedan'];
  const urls = [
    pool[photoIndex % pool.length],
    pool[(photoIndex + 1) % pool.length],
    pool[(photoIndex + 2) % pool.length],
  ];
  return urls.map((url, i) => ({
    id: `photo-${listingId}-${i}`,
    listing_id: listingId,
    url,
    order_index: i,
    is_cover: i === 0,
  }));
}

